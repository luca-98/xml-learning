    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Product p = listAllProduct.get(index);
        p.setName(jTextField1.getText());
        p.setPrice(jTextField2.getText());
        p.setDescription(jTextField3.getText());
        String images = jTextArea1.getText();
        String[] imagesArray = images.split("\n");
        for (String string : imagesArray) {
            p.setImages(string);
        }
         IOModel.writeDOM(listAllProduct);
         this.parent.loadTable(listAllProduct);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

