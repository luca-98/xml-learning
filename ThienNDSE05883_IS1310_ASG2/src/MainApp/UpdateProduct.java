    void load() {
        Product p = listAllProduct.get(index);
        jTextField1.setText(p.getName());
        jTextField2.setText(p.getPrice());
        jTextField3.setText(p.getDescription());
        String img = "";
        for (String image : p.getImages()) {
            img += image + "\n";
        }
        jTextArea1.setText(img);
    }

